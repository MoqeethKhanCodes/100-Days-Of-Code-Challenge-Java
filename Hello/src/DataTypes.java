public class DataTypes {
    public static void main(String[] args){
        int num = 10;
        double num1 = 57.7;
        float num2 = 5.7f;
        char name = 'a';
        String name1 = "Moqeeth Khan";

        System.out.println("Integer: " +num);
        System.out.println("Decimal Value Double: " +num1);
        System.out.println("Float Type: " + num2);
        System.out.println("Character Type: " +name);
        System.out.println("String Type: " +name1);


    }
}
